<?php
session_start();

require_once 'auth.php';

// Проверка, авторизован ли пользователь
if (isset($_SESSION['user_id'])) {
  // Если пользователь авторизован, показать содержимое страницы для авторизованных пользователей
  echo "Добро пожаловать, {$_SESSION['user_name']}! <a href='logout.php'>Выйти</a>";
} else {
  // Если пользователь не авторизован, показать содержимое страницы для неавторизованных пользователей
  echo "Добро пожаловать на наш сайт! <a href='login.php'>Войти</a> или <a href='register.php'>Зарегистрироваться</a>";
}
?>